package br.com.paxtecnologia.pma.relatorio.vo;

public class SemParadasVO {
	
	private Integer diasTrabalhados;
	private Integer paradasEvitadas;
	private Integer diasSemParadas;
	
	public Integer getDiasTrabalhados() {
		return diasTrabalhados;
	}
	public void setDiasTrabalhados(Integer diasTrabalhados) {
		this.diasTrabalhados = diasTrabalhados;
	}
	public Integer getParadasEvitadas() {
		return paradasEvitadas;
	}
	public void setParadasEvitadas(Integer paradasEvitadas) {
		this.paradasEvitadas = paradasEvitadas;
	}
	public Integer getDiasSemParadas() {
		return diasSemParadas;
	}
	public void setDiasSemParadas(Integer diasSemParadas) {
		this.diasSemParadas = diasSemParadas;
	}
	

}
